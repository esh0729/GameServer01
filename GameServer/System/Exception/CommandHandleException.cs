using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GameServer
{
	public class CommandHandleException : Exception
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Member variables

		private short m_nResult;

		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Constructors

		public CommandHandleException(short nResult, string sMessage)
			: base(sMessage)
		{
			m_nResult = nResult;
		}

		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Properties

		public short result
		{
			get { return m_nResult; }
		}
	}
}
